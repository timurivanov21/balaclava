      </p>
      <p class="history-text history-text3">
        Париться в русской бане принято сидя или лежа. Голову принято накрывать полотенцем или специальной шапочкой,
        чтобы волосы не пересушились и не стали ломкими. Первый заход в парилку разогревающий, он не более пяти минут.
        В первое посещение сходит самый грязный и едкий пот. В последующие разы можно сидеть в парной до первых признаков
        усталости. Банные гурманы посещают парную за два часа, раз пять, но неподготовленному человеку за это время хватит
        и трех. После каждого посещения парной можно принять холодный душ, или скупаться в деревянной катке, наполненной
        холодной водой. Контрастные процедуры очень полезны для кожи и для общего тонуса организма. Особенно полезно это
